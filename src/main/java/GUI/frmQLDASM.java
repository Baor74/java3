    public void loadDataTable() {
        try {
            con = KetNoiSQL.openConnettion();
            st = con.createStatement();
            rs = st.executeQuery("select top 3 Students.MaSV , Hoten , TIENGANH , TINHOC , GDTC,(TIENGANH + TINHOC + GDTC)/3 AS \"diem tb\"\n"
                    + "from Students inner join GRADE on Students.MaSV = GRADE.MASV\n"
                    + "order by \"diem tb\" desc;");
            tblModel.setRowCount(0);
            while (rs.next()) {
                Vector row = new Vector();
                row.add(rs.getString(1));
                row.add(rs.getString(2));
                row.add(rs.getFloat(3));
                row.add(rs.getFloat(4));
                row.add(rs.getFloat(5));
                row.add(rs.getFloat(6));
                tblModel.addRow(row);
            }
            tblGradeStudent.setModel(tblModel);
            con.close();
            st.close();
            rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

    public void loadData() {
        try {
            con = KetNoiSQL.openConnettion();
            st = con.createStatement();
            rs = st.executeQuery("select ID , Students.MaSV , HoTen , TIENGANH, TINHOC, GDTC\n"
                    + "from Students inner join GRADE on Students.MaSV = GRADE.MASV");
            list.clear();
            while (rs.next()) {
                int id = rs.getInt(1);
                String maSV = rs.getString(2);
                String hoTen = rs.getString(3);
                float tiengAnh = rs.getFloat(4);
                float tinHoc = rs.getFloat(5);
                float gdtc = rs.getFloat(6);
                float diemTB = (tiengAnh + tinHoc + gdtc) / 3;
                Grade grade = new Grade(id, maSV, hoTen, tiengAnh, tinHoc, gdtc, diemTB);
                list.add(grade);
            }
            current = 0;
            Display();
            rs.close();
            st.close();
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }

    }

    public void loadDiemTB() {
        float ta = Float.parseFloat(txtTA.getText());
        float th = Float.parseFloat(txtTH.getText());
        float gdtc = Float.parseFloat(txtGDTC.getText());
        float tb = (ta + th + gdtc) / 3;
        lbDiemTB.setText(tb + "");
    }

