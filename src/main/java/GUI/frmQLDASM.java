    private void remove() {
        String masv = txtMaSV.getText();

        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa " + masv + " Không?", "Confirm", JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        try {
            con = KetNoiSQL.openConnettion();
            pst = con.prepareStatement("Delete From Grade where MaSV=?");
            pst.setString(1, masv);
            pst.executeUpdate();
            loadData();
            loadDataTable();
            addnew();
        } catch (Exception e) {
        }
    }

    private void update() {
        if (!check()) {
            return;
        }
        try {
            con = KetNoiSQL.openConnettion();
            pst = con.prepareStatement("Update Grade set TiengAnh = ?, TinHoc = ?, GDTC = ? Where MASV = ?");
            pst.setFloat(1, diemTA);
            pst.setFloat(2, diemTH);
            pst.setFloat(3, diemGDTC);
            pst.setString(4, txtMaSV.getText());
            pst.executeUpdate();
            loadData();
            loadDataTable();
            JOptionPane.showMessageDialog(this, "Update thành công!");
        } catch (Exception e) {
        }
    }

    private void save() {
        if (!check()) {
            return;
        }
        try {
            con = KetNoiSQL.openConnettion();
            pst = con.prepareStatement("Insert  Into Grade(MaSV,TiengAnh,TinHoc,GDTC) values(?,?,?,?)");
            pst.setString(1, txtMaSV.getText());
            pst.setFloat(2, diemTA);
            pst.setFloat(3, diemTH);
            pst.setFloat(4, diemGDTC);
            pst.executeUpdate();
            loadData();
            loadDataTable();
            addnew();
            JOptionPane.showMessageDialog(this, "Thêm thành công!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }

    }

    private void addnew() {
        txtName.setText("");
        txtMaSV.setText("");
        txtTA.setText("");
        txtTH.setText("");
        txtGDTC.setText("");
        txtSeach.setText("");
        lbDiemTB.setText("");

    }

    private void seach() {
        int i;
        String maSV;
        maSV = txtSeach.getText();
        for (i = 0; i < list.size(); i++) {
            Grade grade = list.get(i);
            if (grade.getMaSV().equalsIgnoreCase(maSV)) {
                JOptionPane.showMessageDialog(this, "Đã tìm thấy Sinh viên " + maSV, "Thông báo!", JOptionPane.INFORMATION_MESSAGE);
                current = i;
                Display();
                break;
            }
        }
        if (i == list.size()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy Sinh viên " + maSV, "Thông báo!", JOptionPane.INFORMATION_MESSAGE);
            txtSeach.requestFocus();
        }
    }

    private void Display() {
        Grade grade = list.get(current);
        txtMaSV.setText(grade.getMaSV() + "");
        txtName.setText(grade.getHoTen() + "");
        txtTA.setText(grade.getDiemTA() + "");
        txtTH.setText(grade.getDiemTinHoc() + "");
        txtGDTC.setText(grade.getDiemGDTC() + "");
        loadDiemTB();
    }

    public boolean check() {

        try {
            diemGDTC = Float.parseFloat(txtGDTC.getText());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Loi nhap diem GDTC!");
            return false;
        }
        try {
            diemTA = Float.parseFloat(txtTA.getText());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Loi nhap diem Tieng Anh!");
            return false;
        }
        try {
            diemTH = Float.parseFloat(txtTH.getText());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Loi nhap diem Tin hoc!");
            return false;
        }
        return true;
    }
