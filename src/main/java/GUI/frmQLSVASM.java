    private void save() {
        String sql = "Insert into Students values(?,?,?,?,?,?,?)";
        try {
            con = KetNoiSQL.openConnettion();
            pst = con.prepareStatement(sql);
            pst.setString(1, txtMaSV.getText());
            pst.setString(2, txtName.getText());
            pst.setString(3, txtEmail.getText());
            pst.setString(4, txtSoDT.getText());
            if (rNam.isSelected()) {
                pst.setBoolean(5, true);
            }
            if (rNu.isSelected()) {
                pst.setBoolean(5, false);
            }
            pst.setString(6, txtAddress.getText());
            pst.setString(7, filephoto);
            pst.executeUpdate();
            pst.close();
            con.close();
            load_data();
            showDetail();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }

    }

    private void update() {
        int row = tblSudent.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn dữ liệu để Update!");
            return;
        }
        try {
            con = KetNoiSQL.openConnettion();
            pst = con.prepareStatement("Update Students set Hoten = ?, Email = ?, "
                    + "SoDT = ?, GioiTinh = ?, DiaChi = ?, Hinh = ? where MaSV = ?");

            pst.setString(1, txtName.getText());
            pst.setString(2, txtEmail.getText());
            pst.setString(3, txtSoDT.getText());
            if (rNam.isSelected()) {
                pst.setBoolean(4, true);
            }
            if (rNu.isSelected()) {
                pst.setBoolean(4, false);
            }
            pst.setString(5, txtAddress.getText());
            pst.setString(6, filephoto);
            pst.setString(7, txtMaSV.getText());
            pst.executeUpdate();
            pst.close();
            con.close();
            load_data();
            

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }

    }

    private void reset() {
        txtAddress.setText("");
        txtEmail.setText("");
        txtMaSV.setText("");
        txtName.setText("");
        txtSoDT.setText("");
        btnGroup.clearSelection();
        lbPhoto.setIcon(null);
    }

    private void delete() {
        int row = tblSudent.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn dữ liệu để xóa!");
            return;
        }
        int ret = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa sinh viên " + tblSudent.getValueAt(row, 1), "Confirm",
                JOptionPane.YES_NO_OPTION);
        if (ret == JOptionPane.NO_OPTION) {
            return;
        }
        if (ret == JOptionPane.YES_OPTION) {
            String id = tblSudent.getValueAt(row, 0).toString();
            try {
                con = KetNoiSQL.openConnettion();
                pst = con.prepareStatement("Delete from Students where MaSV = ?");
                pst.setString(1, id);
                pst.executeUpdate();
                tblModel.removeRow(row);
                tblSudent.setModel(tblModel);
                JOptionPane.showMessageDialog(this, "Xóa thành công!");

            } catch (Exception e) {
            }
        }
    }
