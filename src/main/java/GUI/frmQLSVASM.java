    private void showDetail() {
        int row = tblSudent.getSelectedRow();
        txtMaSV.setText(tblSudent.getValueAt(row, 0).toString());
        txtName.setText(tblSudent.getValueAt(row, 1).toString());
        txtEmail.setText(tblSudent.getValueAt(row, 2).toString());
        txtSoDT.setText(tblSudent.getValueAt(row, 3).toString());
        boolean gt = Boolean.parseBoolean(tblSudent.getValueAt(row, 4).toString());
        if (gt == true) {
            rNam.setSelected(true);
            rNu.setSelected(false);
        } else {
            rNu.setSelected(true);
            rNam.setSelected(false);
        }
        txtAddress.setText(tblSudent.getValueAt(row, 5).toString());
        load_photo(tblSudent.getValueAt(row, 6).toString());
    }

    private void load_photo(String hinh) {
        ImageIcon img = new ImageIcon("src\\main\\java\\Icons\\" + hinh);
        Image im = img.getImage();
        ImageIcon icon = new ImageIcon(im.getScaledInstance(120, 140, 0));
        lbPhoto.setIcon(icon);
    }

    private void photo() {
        JFileChooser filechooser = new JFileChooser("src\\main\\java\\Icons\\");
        int result = filechooser.showOpenDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File file = filechooser.getSelectedFile();
            String fullPath = file.getAbsolutePath();
            filephoto = filechooser.getSelectedFile().getName();
            load_photo(filephoto);
            try {
                Path src = Paths.get(fullPath);
                Path dest = Paths.get("src\\main\\java\\Icons\\" + filephoto);
                Files.copy(src, dest, StandardCopyOption.REPLACE_EXISTING);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
            
        }
    }

