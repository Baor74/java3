    public void load_data() {
        String sql = "select * from Students";
        try {
            con = KetNoiSQL.openConnettion();
            st = con.createStatement();
            rs = st.executeQuery(sql);

            tblModel.setRowCount(0);
            while (rs.next()) {
                Vector sv = new Vector();
                sv.add(rs.getString(1));
                sv.add(rs.getString(2));
                sv.add(rs.getString(3));
                sv.add(rs.getString(4));
                sv.add(rs.getBoolean(5));
                sv.add(rs.getString(6));
                sv.add(rs.getString(7));
                tblModel.addRow(sv);
            }
            tblSudent.setModel(tblModel);
            con.close();
            st.close();
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

